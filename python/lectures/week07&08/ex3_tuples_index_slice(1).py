#Indexing, Slicing
#Because tuples are sequences, indexing and slicing work the same way
#for tuples as they do for strings. 

ListofTuples = ('exam', 'Example', 'Ex!')

print(ListofTuples[2])	#Offsets start at zero
print(ListofTuples[-2])	#Negative: count from the right
print(ListofTuples[1:])	#Slicing fetches sections
