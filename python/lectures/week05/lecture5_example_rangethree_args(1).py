#illustrates range function with three arguments
#first arg is the starting value
#secnd arg is the ending value
#third argument is the step or increment value
#could do decrements as well!!!

for num in range (1, 10, 2):
    print ("1:", num)

for num1 in range (10, 1, -2):
    print ("2:", num1)

for num2 in range (10, 1, -1):
    print ("3:", num2)
    
